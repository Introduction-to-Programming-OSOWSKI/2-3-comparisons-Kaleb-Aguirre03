#Define the Function greaterThan()
def greaterThan(x, y):
    if x > y:
        return True

    else:
        return False

#Define the Function lessThan()
def lessThan(x, y):
    if x < y:
        return True

    else:
        return False

#Define the Function equalTo()
def equalTo(x, y):
    if x == y:
        return True

    else:
        return False

